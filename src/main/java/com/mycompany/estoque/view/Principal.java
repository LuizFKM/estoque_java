    private void atualizarTabelaCategoria(){
        DefaultTableModel tabela = (DefaultTableModel) tblCategoria.getModel();
        
        tabela.setRowCount(0);
        
        for(Categoria c : cDAO.listar() ){
            tabela.addRow(new Object[]{c.getId(), c.getNome()});
        }
    }
    
    private void configurarTabela(){
        tblCategoria.getColumnModel().getColumn(0).setPreferredWidth(25);
        tblCategoria.getColumnModel().getColumn(1).setPreferredWidth(300);
        
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        tblCategoria.getColumnModel().getColumn(0).setCellRenderer(centralizado);
        tblCategoria.getColumnModel().getColumn(1).setCellRenderer(centralizado);
    }

